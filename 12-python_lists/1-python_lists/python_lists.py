# List are created using square brackets
thislist = ["apple", "banana", "cherry"]

# List are ordered, changable, and allow duplicate values
thislist = ['apple', 'banana', 'cherry', 'apple', 'cherry']

# To determine how many items are in a list use the len() function:
thislist = ['apple', 'banana', 'cherry']
print(len(thislist))

# List items can be of any datatype
list1 = ['apple', 'banana', 'cherry']
list2 = [1,5,7,9,3]
list3 = [True, False, False]

# A list can contain different datatypes
list1 = ['abc', 34, True, 40, "male"]

# What is the datatype of a list?
mylist = ['apple', 'banana', 'cherry']
print(type(mylist))

# It is also possible to use the list constructor to create a new list
thislist = (("apple", "banana", "cherry"))
