# Python - Sort Lists
# Sort List Alphanumerically
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

# sort the list numerically
thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist)

# You can sort descending (default=ascending) with the keyword argument reverse = True:
thislist = ['orange', 'mango', 'kiwi', 'pineapple', 'banana']
thislist.sort(reverse=True)
print(thislist)

#You can sort the list in decending order
thislist = [100,50,65,82,23]
print(thislist)

